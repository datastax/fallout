package com.datastax.fallout.util.cockpit;

import java.util.Collection;

import com.fasterxml.jackson.core.JsonProcessingException;
import com.fasterxml.jackson.databind.ObjectMapper;
import com.fasterxml.jackson.databind.ObjectWriter;

public class CockpitAggregatesWriter
{
    private static final ObjectWriter JSON_WRITER = new ObjectMapper().writer();

    public static <T> String convertToJson(Collection<Aggregate<T>> aggregates) throws JsonProcessingException
    {
        return JSON_WRITER.writeValueAsString(aggregates);
    }
}
